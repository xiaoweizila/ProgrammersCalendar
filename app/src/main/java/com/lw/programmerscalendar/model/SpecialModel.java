package com.lw.programmerscalendar.model;

public class SpecialModel {
    public int date;
    public String type;
    public String name;
    public String description;

    public SpecialModel(int date, String type, String name, String description) {
        this.date = date;
        this.type = type;
        this.name = name;
        this.description = description;
    }
}
